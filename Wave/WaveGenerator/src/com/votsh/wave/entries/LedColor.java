package com.votsh.wave.entries;

/**
 *
 * @author Luis
 */
public class LedColor {

    public double r;
    public double g;
    public double b;

    /**
     *
     */
    public LedColor() {
    }

    /**
     *
     * @param R
     * @param G
     * @param B
     */
    public LedColor(double R, double G, double B) {
        r = R;
        g = G;
        b = B;
    }

}
